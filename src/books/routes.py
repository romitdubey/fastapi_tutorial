from fastapi import APIRouter, status, Depends
from fastapi.exceptions import HTTPException
from .book_data import books
from .schemas import Book, BookUpdateModel, BookCreateModel
from src.db.main import get_session
from sqlmodel.ext.asyncio.session import AsyncSession
from .service import BookService
from typing import List
import uuid
from src.auth.dependencies import AcessTokenBearer

book_router = APIRouter()
book_service = BookService()
access_token_bearer = AcessTokenBearer()

@book_router.get("/", response_model=List[Book])
async def get_all_books(session:AsyncSession = Depends(get_session),users_details= Depends(access_token_bearer)):
    print("User details from token:", users_details)
    books = await book_service.get_all_books(session=session)
    return books

@book_router.post("/",status_code=status.HTTP_201_CREATED, response_model=Book)
async def create_a_book(book_data:BookCreateModel,session:AsyncSession = Depends(get_session),users_details= Depends(access_token_bearer))->dict:
    new_book = await book_service.create_book(book_data, session=session)
    return new_book 


@book_router.get("/{book_uid}", response_model=Book)
async def get_book(book_uid:str,session:AsyncSession = Depends(get_session),users_details= Depends(access_token_bearer)) ->dict:
    book_one = await book_service.get_book(book_uid,session=session)
    if book_one is not None:
        return book_one
    else:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Book not found")

@book_router.patch("/{book_uid}", response_model=Book)
async def update_book(book_uid:str,book_update_data:BookUpdateModel,session:AsyncSession = Depends(get_session),users_details= Depends(access_token_bearer)) ->dict:
    updated_book = await book_service.update_book(book_uid, book_update_data, session=session)
    if updated_book is not None:
        return updated_book
    else:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Book not found")


@book_router.delete("/{book_uid}", status_code = status.HTTP_204_NO_CONTENT) 
async def delete_book(book_uid:str,session:AsyncSession = Depends(get_session),users_details= Depends(access_token_bearer)):
    deleted_book = await book_service.delete_book(book_uid, session=session)
    if deleted_book is None:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Book not found")
    else:
        return {}